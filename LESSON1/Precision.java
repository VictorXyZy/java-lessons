package LESSON1;

public class Precision {
    public static void main(String[] args) {
        // %[.precision]

        double price1 = 9.99;
        double price2 = 100.4;
        double price3 = 12.34;

        System.out.printf("%.2f\n", price1);
        System.out.printf("%.2f\n", price2);
        System.out.printf("%.2f\n", price3);

    }

}
